"""Layered process verdict.

Combines the selection-rule analysis, kinematics, and tree-level reachability
into a single graded answer that says not just whether a process happens, but
through what kind of physics. The ordering of the cascade encodes a hierarchy:
exact gauge symmetries first, then kinematics, then the accidental/approximate
global symmetries, then explicit SM dynamics.
"""
from __future__ import annotations

from dataclasses import dataclass
from enum import Enum

from nsm.particles import particle
from nsm.selection import analyze
from nsm.processes import TreeSearchStatus, tree_level_search


class Verdict(Enum):
    FORBIDDEN_BY_EXACT_SYMMETRY = "forbidden by an exact gauge symmetry (charge/color)"
    KINEMATICALLY_FORBIDDEN = "forbidden by energy conservation (final state too heavy)"
    ALLOWED_BY_HIGHER_DIM_OPERATOR = "not generated by the configured perturbative renormalizable vertex set"
    ALLOWED_ONLY_WITH_MIXING_OR_LOOPS = "violates lepton flavor; only via mixing/loops, strongly suppressed"
    ALLOWED_AT_TREE_LEVEL = "allowed at tree level via an explicit SM vertex"
    ALLOWED_BY_CONSERVATION_BUT_NO_SM_VERTEX = "conserves everything, but no SM tree diagram connects it"
    UNDERMODELLED = "involves a particle or structure outside the current model"


@dataclass
class ProcessResult:
    verdict: Verdict
    mediators: list | None
    detail: str
    report: object  # ConservationReport, or None when undermodelled


def _total_mass(names):
    masses = [particle(n).mass_mev for n in names]
    if any(m is None for m in masses):
        return None
    return sum(masses)


def classify(initial, final, extra_vertices=()) -> ProcessResult:
    initial, final = list(initial), list(final)  # materialize: used multiple times
    if not initial or not final:
        return ProcessResult(Verdict.UNDERMODELLED, None,
                             "empty initial or final state", None)
    try:
        for n in list(initial) + list(final):
            particle(n)
    except KeyError as e:
        return ProcessResult(Verdict.UNDERMODELLED, None, str(e), None)

    r = analyze(initial, final)

    # 1. Exact gauge symmetries.
    if not r.charge_conserved or not r.color_conserved:
        which = "electric charge" if not r.charge_conserved else "color"
        return ProcessResult(Verdict.FORBIDDEN_BY_EXACT_SYMMETRY, None,
                             f"{which} is not conserved", r)

    # 2. Kinematics (only meaningful for a single-particle initial state: a decay
    #    or isolated one-particle transition).
    if len(list(initial)) == 1:
        m_i, m_f = _total_mass(initial), _total_mass(final)
        if m_i is not None and m_f is not None:
            if len(final) == 1 and m_f != m_i:
                return ProcessResult(Verdict.KINEMATICALLY_FORBIDDEN, None,
                                     f"1->1 transition changes rest mass "
                                     f"({m_i:.3f} -> {m_f:.3f} MeV); four-momentum "
                                     "cannot be conserved for an isolated particle", r)
            if len(final) > 1 and m_f >= m_i:
                return ProcessResult(Verdict.KINEMATICALLY_FORBIDDEN, None,
                                     f"final-state mass {m_f:.3f} MeV >= initial "
                                     f"{m_i:.3f} MeV: no decay phase space", r)

    # 3. Accidental global symmetries of the renormalizable SM. B-L is the
    #    combination that survives the leading (dim-6) operators, so whether it
    #    is also violated tells us *which* kind of new operator is required.
    if not r.baryon_conserved or not r.lepton_conserved:
        deltas = f"DeltaB={r.delta_baryon}, DeltaL={r.delta_lepton}, Delta(B-L)={r.delta_bminusl}"
        if r.bminusl_conserved:
            detail = ("not generated by the configured perturbative renormalizable "
                      f"vertex set ({deltas}); B-L conserved, compatible with "
                      "higher-dimensional operators such as proton decay")
        else:
            detail = ("not generated by the configured perturbative renormalizable "
                      f"vertex set ({deltas}); B-L violated, requiring a "
                      "B-L-violating mechanism in this structural model")
        return ProcessResult(Verdict.ALLOWED_BY_HIGHER_DIM_OPERATOR, None, detail, r)

    # 4. Approximate symmetry: individual lepton flavor.
    if not r.flavor_conserved:
        return ProcessResult(Verdict.ALLOWED_ONLY_WITH_MIXING_OR_LOOPS, None,
                             "lepton flavor violated (total lepton number intact)", r)

    # 5. Everything is conserved: is there an explicit SM tree diagram?
    tree = tree_level_search(initial, final, extra_vertices=extra_vertices)
    if tree.status is TreeSearchStatus.BUDGET_EXCEEDED:
        return ProcessResult(Verdict.UNDERMODELLED, None,
                             f"tree search budget exceeded: {tree.detail}", r)
    if tree.status is TreeSearchStatus.FOUND:
        mediators = tree.mediators or []
        via = ", ".join(mediators) if mediators else "a single vertex"
        return ProcessResult(Verdict.ALLOWED_AT_TREE_LEVEL, mediators,
                             f"tree diagram via {via}", r)

    # The tree search runs over FUNDAMENTAL fields; composite hadrons are not
    # decomposed into their quark constituents, so a process that is tree-level
    # only at the quark level (e.g. beta decay) is reported here, honestly
    # flagged rather than mislabeled.
    composite = [n for n in list(initial) + list(final) if particle(n).constituents]
    if composite:
        detail = ("conserves all charges; involves composite states "
                  f"({', '.join(sorted(set(composite)))}) not expanded into quarks, "
                  "so a quark-level tree diagram may still exist")
    else:
        detail = ("conserves all charges, but no tree was found in the configured "
                  "vertex subset (which omits some multi-gauge, scalar self-, and "
                  "quartic vertices); this does not by itself imply the process is "
                  "loop-level")
    return ProcessResult(Verdict.ALLOWED_BY_CONSERVATION_BUT_NO_SM_VERTEX, None, detail, r)
